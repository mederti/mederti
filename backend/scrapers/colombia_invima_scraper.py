"""
INVIMA Colombia — Listado de Abastecimiento y Desabastecimiento de Medicamentos
--------------------------------------------------------------------------------
Source:  INVIMA (Instituto Nacional de Vigilancia de Medicamentos y Alimentos)
URL:     https://www.invima.gov.co/productos-vigilados/medicamentos-y-productos-biologicos/desabastecimientos

INVIMA publishes a monthly PDF listing every active ingredient / product under
shortage surveillance, with an explicit Spanish-language status taxonomy:

    "En monitorización"              — being monitored (early tier; not yet
                                        confirmed at-risk or out of stock)
    "En riesgo de desabastecimiento" — at risk of shortage
    "Desabastecido" (+ *,**,***,**** footnote markers referencing endnotes)
                                      — currently out of stock
    "No desabastecido"               — no longer in shortage (resolved)
    "Descontinuado"                  — permanently discontinued by the MAH
    "No comercializado"              — never actually marketed / withdrawn

This is one of the few genuinely structured, tabular shortage feeds we've
found in Latin America (verified live 2026-07-02 against the Enero 2026
report, 91 pages / ~770 tracked line items).

Landing page quirk: older monthly reports link straight to a `.pdf` on
invima.gov.co; newer ones (~2025 onward) link to an internal `/biblioteca/...`
document page that itself contains a `/biblioteca/download/<id>` link to the
actual PDF. fetch() follows both link shapes.

PDF table quirk #1: pdfplumber's table extraction repeats the section title
as row 0 of every page's table, and repeats the column header row
("No.", "Nombre del Medicamento", "ATC", ...) at the top of each page. A
single tracked product can span multiple physical table rows when several
manufacturers report separately (subsequent rows leave No./Nombre/Estado
blank) — normalize() forward-fills those columns from the last fully
populated row.

PDF table quirk #2 (verified live 2026-07-02 — the Mayo 2026 report,
generated by a different pipeline than earlier months): pdfplumber's
default `page.extract_tables()` returns badly scrambled/interleaved text in
this report — its geometric word-ordering heuristic misorders characters
that the PDF itself draws in a different visual arrangement. The literal
content-stream draw order IS correct reading order for this document.
Fix: use `page.find_tables()` for cell geometry only, then re-extract each
cell's text ourselves via `page.crop(bbox).extract_text(use_text_flow=True)`
— this reads characters in content-stream order instead of re-sorting by
position, and produces clean text on both the well-behaved (Enero 2026) and
the previously-garbled (Mayo 2026) reports alike.

Data source UUID:  10000000-0000-0000-0000-000000000108
Country:           Colombia
Country code:      CO
Confidence:        85/100 (seeded in migration 064)

Cron:  Not yet wired — integrated separately per project convention.
"""

from __future__ import annotations

import io
import re
from datetime import date, datetime, timezone
from typing import Any

from backend.scrapers.base_scraper import BaseScraper, ScraperError
